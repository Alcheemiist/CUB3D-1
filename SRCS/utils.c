#include "../include/cub3D.h"

void	ft_puterror(char *error)
{
	char *e;
	e = "\033[1;31m Error\n >> \033[0;31m";
	ft_putstr_fd(e, 1);
	ft_putstr_fd(error, 1);
	exit(-1);
}